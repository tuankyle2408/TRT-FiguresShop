package com.example.trt_figuresshop.Model;

public class ProductType {
    int id;
    String tenfigure;
    String hinh;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenfigure() {
        return tenfigure;
    }

    public void setTenfigure(String tenfigure) {
        this.tenfigure = tenfigure;
    }

    public String getHinh() {
        return hinh;
    }

    public void setHinh(String hinh) {
        this.hinh = hinh;
    }
}
